package kr.happyjob.study.sup.model;



public class AwardMgtModel  {
	
	private int award_no;		// 상장번호
	private String loginID;		// loginID
	private String award_nm;	// 상장명
	private String award_date;	// 수상일자
	
	@Override
	public String toString() {
		return "AwardMgtModel [award_no=" + award_no + ", loginID=" + loginID + ", award_nm=" + award_nm
				+ ", award_date=" + award_date + "]";
	}

	public int getAward_no() {
		return award_no;
	}

	public void setAward_no(int award_no) {
		this.award_no = award_no;
	}

	public String getLoginID() {
		return loginID;
	}

	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}

	public String getAward_nm() {
		return award_nm;
	}

	public void setAward_nm(String award_nm) {
		this.award_nm = award_nm;
	}

	public String getAward_date() {
		return award_date;
	}

	public void setAward_date(String award_date) {
		this.award_date = award_date;
	}
	
}
